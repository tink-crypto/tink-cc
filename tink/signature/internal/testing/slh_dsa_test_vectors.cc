absl::StatusOr<SlhDsaPrivateKey> CreatePrivateKey(
    SlhDsaParameters::HashType hash_type, int private_key_size_in_bytes,
    SlhDsaParameters::SignatureType signature_type,
    SlhDsaParameters::Variant variant, absl::string_view pub_hex,
    absl::string_view priv_hex, absl::optional<int> id_requirement) {
  absl::StatusOr<SlhDsaParameters> parameters = SlhDsaParameters::Create(
      hash_type, private_key_size_in_bytes, signature_type, variant);
  if (!parameters.ok()) {
    return parameters.status();
  }

  absl::StatusOr<SlhDsaPublicKey> public_key =
      SlhDsaPublicKey::Create(*parameters, HexDecodeOrDie(pub_hex),
                              id_requirement, GetPartialKeyAccess());
  if (!public_key.ok()) {
    return public_key.status();
  }

  return SlhDsaPrivateKey::Create(
      *public_key,
      RestrictedData(HexDecodeOrDie(priv_hex), InsecureSecretKeyAccess::Get()),
      GetPartialKeyAccess());
}

