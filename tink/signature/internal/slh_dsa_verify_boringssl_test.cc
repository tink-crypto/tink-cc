struct TestVector {
  std::string test_name;
  SlhDsaParameters::HashType hash_type;
  int private_key_size_in_bytes;
  SlhDsaParameters::SignatureType signature_type;
  SlhDsaParameters::Variant variant;
  std::string hex_private_key;
  std::string hex_public_key;
  std::string hex_message;
  std::string hex_signature;
};

using SlhDsaVerifyBoringSslTestVectorTest = testing::TestWithParam<TestVector>;

TEST_P(SlhDsaVerifyBoringSslTestVectorTest, SignAndVerify) {
  const TestVector& param = GetParam();
  std::string private_bytes = test::HexDecodeOrDie(param.hex_private_key);
  std::string public_key_bytes = test::HexDecodeOrDie(param.hex_public_key);
  std::string message_bytes = test::HexDecodeOrDie(param.hex_message);
  std::string signature_bytes = test::HexDecodeOrDie(param.hex_signature);

  absl::StatusOr<SlhDsaParameters> parameters =
      SlhDsaParameters::Create(param.hash_type, param.private_key_size_in_bytes,
                               param.signature_type, param.variant);
  ASSERT_THAT(parameters, IsOk());

  absl::StatusOr<SlhDsaPublicKey> public_key = SlhDsaPublicKey::Create(
      *parameters, public_key_bytes, absl::nullopt, GetPartialKeyAccess());
  ASSERT_THAT(public_key, IsOk());

  absl::StatusOr<std::unique_ptr<PublicKeyVerify>> verifier =
      NewSlhDsaVerifyBoringSsl(*public_key);
  ASSERT_THAT(verifier, IsOk());

  EXPECT_THAT((*verifier)->Verify(signature_bytes, message_bytes), IsOk());

  // Create a new signature with the private key and verify.
  absl::StatusOr<SlhDsaPrivateKey> private_key = SlhDsaPrivateKey::Create(
      *public_key,
      RestrictedData(private_bytes, InsecureSecretKeyAccess::Get()),
      GetPartialKeyAccess());
  ASSERT_THAT(private_key, IsOk());

  absl::StatusOr<std::unique_ptr<PublicKeySign>> signer =
      NewSlhDsaSignBoringSsl(*private_key);
  ASSERT_THAT(signer, IsOk());

  absl::StatusOr<std::string> sig = (*signer)->Sign(message_bytes);
  ASSERT_THAT(sig, IsOk());
  EXPECT_THAT((*verifier)->Verify(*sig, message_bytes), IsOk());
}

